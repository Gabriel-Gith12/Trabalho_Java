    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:

        DefaultTableModel modelo = (DefaultTableModel) jProdutos.getModel();
        modelo.setNumRows(0);

        SaldoDAO dao = new SaldoDAO();
        String descricao = jComboBox1.getSelectedItem().toString();

        if (descricao.equals("Todos")) {
            for (Produto h : dao.read()) {
                modelo.addRow(new Object[]{
                    h.getDescricaoProduto(),
                    h.getSaldo()
                });
            }
        } else {
            modelo.addRow(new Object[]{descricao, "Saldo"});
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

