    public void readJTable() {

        DefaultTableModel modelo = (DefaultTableModel) jProdutos.getModel();
        modelo.setNumRows(0);
        SaldoDAO dao = new SaldoDAO();

        for (Produto h : dao.read()) {

            modelo.addRow(new Object[]{
                h.getDescricaoProduto(),
                h.getSaldo()
            });
        }
    }

